function deterministicId(prefix: string, seed: string): string {
  const hash = crypto.createHash("sha256").update(seed).digest("hex").substring(0, 8).toUpperCase();
  return `${prefix}-${hash}`;
}

async function generateQRDataUrl(text: string): Promise<string> {
  return QRCode.toDataURL(text, { width: 200, margin: 1 });
}

function buildPdfBuffer(
  data: {
    brandLabel: string;
    verifyDomain: string;
    vaultId: string;
    wowShieldId: string;
    projectName: string;
    clientName: string;
    address: string;
    jobType: string;
    completionDate: string;
    value: string;
    audits: { type: string; result: string; id: string }[];
    artifacts: string[];
    qrImageBuffer: Buffer;
  }
): Promise<Buffer> {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: "LETTER", margin: 0 });
    const chunks: Buffer[] = [];
    doc.on("data", (chunk: Buffer) => chunks.push(chunk));
    doc.on("end", () => resolve(Buffer.concat(chunks)));
    doc.on("error", reject);

    const pageW = 612;

    doc.rect(0, 0, pageW, 80).fill("#1a1a2e");
    doc.fontSize(28).font("Helvetica-Bold").fillColor("#ffffff").text("TRUTH-VAULT CERTIFICATE", 40, 20, { width: pageW - 120, align: "left" });
    doc.fontSize(10).font("Helvetica").fillColor("#aaaaaa").text("Official Structural Integrity Record", 40, 55, { width: pageW - 120 });

    doc.image(data.qrImageBuffer, pageW - 100, 10, { width: 60, height: 60 });

    doc.fillColor("#333333");
    let y = 100;

    doc.rect(40, y, pageW - 80, 1).fill("#dddddd");
    y += 15;

    doc.fontSize(9).font("Helvetica").fillColor("#888888").text("VAULT ID", 40, y);
    doc.text("WOW-SHIELD ID", 220, y);
    doc.text("PLATFORM", 400, y);
    y += 14;
    doc.fontSize(11).font("Helvetica-Bold").fillColor("#1a1a2e").text(data.vaultId, 40, y);
    doc.text(data.wowShieldId, 220, y);
    doc.text(data.brandLabel, 400, y);
    y += 28;

    doc.rect(40, y, pageW - 80, 1).fill("#dddddd");
    y += 15;

    doc.fontSize(16).font("Helvetica-Bold").fillColor("#1a1a2e").text(data.projectName, 40, y, { width: pageW - 80 });
    y += 24;

    const fields = [
      ["CLIENT", data.clientName],
      ["ADDRESS", data.address],
      ["JOB TYPE", data.jobType],
      ["COMPLETION DATE", data.completionDate],
      ["CONTRACT VALUE", data.value],
    ];

    for (const [label, val] of fields) {
      doc.fontSize(8).font("Helvetica").fillColor("#888888").text(label, 40, y);
      doc.fontSize(11).font("Helvetica").fillColor("#333333").text(val, 160, y);
      y += 18;
    }

    y += 10;
    doc.rect(40, y, pageW - 80, 1).fill("#dddddd");
    y += 12;

    doc.rect(40, y, pageW - 80, 22).fill("#f0f4f0");
    doc.fontSize(10).font("Helvetica-Bold").fillColor("#1a6b3a").text("EyeSpyR AI AUDIT SUMMARY", 50, y + 6, { width: pageW - 100 });
    y += 30;

    for (const audit of data.audits) {
      const color = audit.result === "PASS" ? "#1a6b3a" : "#b45309";
      doc.fontSize(10).font("Helvetica").fillColor("#333333").text(`- ${audit.type}: `, 50, y, { continued: true });
      doc.fillColor(color).text(audit.result, { continued: true });
      doc.fillColor("#999999").text(`  (ID: ${audit.id})`);
      y += 16;
    }

    y += 10;
    doc.rect(40, y, pageW - 80, 1).fill("#dddddd");
    y += 12;

    doc.rect(40, y, pageW - 80, 22).fill("#f0f0f5");
    doc.fontSize(10).font("Helvetica-Bold").fillColor("#1a1a2e").text("STORED ARTIFACTS (7-Year Guarantee)", 50, y + 6, { width: pageW - 100 });
    y += 30;

    for (const item of data.artifacts) {
      doc.fontSize(10).font("Helvetica").fillColor("#333333").text(`[x]  ${item}`, 50, y);
      y += 15;
    }

    y += 20;
    doc.rect(40, y, pageW - 80, 1).fill("#1a1a2e");
    y += 10;
    doc.fontSize(8).font("Helvetica").fillColor("#999999").text(
      `This certificate was generated by ${data.brandLabel} Truth-Vault system. Verify authenticity at ${data.verifyDomain}/v/${data.vaultId}`,
      40, y, { width: pageW - 80, align: "center" }
    );

    doc.end();
  });
}

function getResend(): Resend | null {
  const key = process.env.RESEND_API_KEY;
  if (!key) return null;
  return new Resend(key);
}

function isValidEmail(email: string): boolean {
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email) && email.length <= 254;
}

