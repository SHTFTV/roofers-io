function usePageTitle(title: string) {
  useEffect(() => { document.title = title; }, [title]);
}

function usePageMeta(description: string, canonicalPath?: string, ogTitle?: string) {
  useEffect(() => {
    const setMeta = (name: string, content: string, attr = "name") => {
      let el = document.querySelector(`meta[${attr}="${name}"]`) as HTMLMetaElement;
      if (!el) { el = document.createElement("meta"); el.setAttribute(attr, name); document.head.appendChild(el); }
      el.content = content;
    };
    setMeta("description", description);
    const fullUrl = canonicalPath ? `https://roofers.io${canonicalPath}` : "https://roofers.io/";
    let canonical = document.querySelector('link[rel="canonical"]') as HTMLLinkElement;
    if (!canonical) { canonical = document.createElement("link"); canonical.rel = "canonical"; document.head.appendChild(canonical); }
    canonical.href = fullUrl;
    setMeta("og:title", ogTitle || document.title, "property");
    setMeta("og:description", description, "property");
    setMeta("og:type", "website", "property");
    setMeta("og:url", fullUrl, "property");
    setMeta("og:site_name", "Roofers.io", "property");
    setMeta("og:image:alt", ogTitle ? `Professional roofing services — ${ogTitle}` : "Roofers.io — Professional Vetted Roofing Contractors", "property");
  }, [description, canonicalPath, ogTitle]);
}

function useStructuredData(data: object) {
  useEffect(() => {
    const id = "structured-data-jsonld";
    let script = document.getElementById(id) as HTMLScriptElement;
    if (!script) { script = document.createElement("script"); script.id = id; script.type = "application/ld+json"; document.head.appendChild(script); }
    script.textContent = JSON.stringify(data);
    return () => { script.remove(); };
  }, [JSON.stringify(data)]);
}

