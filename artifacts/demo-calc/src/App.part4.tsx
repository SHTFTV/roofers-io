function RoofSketchCanvas({ address, satelliteUrl, onComplete }: { address: string; satelliteUrl: string; onComplete: (imageData: string, estimatedSqft: number) => void }) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const [points, setPoints] = useState<{ x: number; y: number }[]>([]);
  const [allShapes, setAllShapes] = useState<{ x: number; y: number }[][]>([]);
  const [tool, setTool] = useState<"draw" | "pointer">("draw");
  const containerRef = useRef<HTMLDivElement>(null);

  const getPos = useCallback((e: React.MouseEvent<HTMLCanvasElement> | React.TouchEvent<HTMLCanvasElement>) => {
    const canvas = canvasRef.current;
    if (!canvas) return { x: 0, y: 0 };
    const rect = canvas.getBoundingClientRect();
    const scaleX = canvas.width / rect.width;
    const scaleY = canvas.height / rect.height;
    if ("touches" in e) {
      const touch = e.touches[0] || e.changedTouches[0];
      return { x: (touch.clientX - rect.left) * scaleX, y: (touch.clientY - rect.top) * scaleY };
    }
    return { x: ((e as React.MouseEvent).clientX - rect.left) * scaleX, y: ((e as React.MouseEvent).clientY - rect.top) * scaleY };
  }, []);

  const drawAll = useCallback((currentPoints?: { x: number; y: number }[]) => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    ctx.clearRect(0, 0, canvas.width, canvas.height);

    const drawShape = (pts: { x: number; y: number }[], closed: boolean) => {
      if (pts.length < 2) return;
      ctx.beginPath();
      ctx.moveTo(pts[0].x, pts[0].y);
      for (let i = 1; i < pts.length; i++) {
        ctx.lineTo(pts[i].x, pts[i].y);
      }
      if (closed) {
        ctx.closePath();
        ctx.fillStyle = "rgba(239, 68, 68, 0.15)";
        ctx.fill();
      }
      ctx.strokeStyle = "#ef4444";
      ctx.lineWidth = 2.5;
      ctx.setLineDash([]);
      ctx.stroke();

      for (const pt of pts) {
        ctx.beginPath();
        ctx.arc(pt.x, pt.y, 4, 0, Math.PI * 2);
        ctx.fillStyle = "#ef4444";
        ctx.fill();
        ctx.strokeStyle = "#0a0a0f";
        ctx.lineWidth = 1.5;
        ctx.stroke();
      }
    };

    allShapes.forEach(shape => drawShape(shape, true));
    if (currentPoints && currentPoints.length > 0) {
      drawShape(currentPoints, false);
      if (currentPoints.length > 0) {
        const first = currentPoints[0];
        ctx.beginPath();
        ctx.setLineDash([4, 4]);
        ctx.strokeStyle = "rgba(239, 68, 68, 0.4)";
        ctx.lineWidth = 1;
        ctx.moveTo(currentPoints[currentPoints.length - 1].x, currentPoints[currentPoints.length - 1].y);
        ctx.lineTo(first.x, first.y);
        ctx.stroke();
        ctx.setLineDash([]);
      }
    }
  }, [allShapes]);

  useEffect(() => { drawAll(points); }, [points, allShapes, drawAll]);

  const handleCanvasClick = useCallback((e: React.MouseEvent<HTMLCanvasElement>) => {
    if (tool !== "draw") return;
    const pos = getPos(e);

    if (points.length > 2) {
      const first = points[0];
      const dist = Math.sqrt((pos.x - first.x) ** 2 + (pos.y - first.y) ** 2);
      if (dist < 15) {
        setAllShapes(prev => [...prev, points]);
        setPoints([]);
        return;
      }
    }
    setPoints(prev => [...prev, pos]);
  }, [tool, points, getPos]);

  const totalPixels = allShapes.reduce((sum, shape) => {
    if (shape.length < 3) return sum;
    let area = 0;
    for (let i = 0; i < shape.length; i++) {
      const j = (i + 1) % shape.length;
      area += shape[i].x * shape[j].y - shape[j].x * shape[i].y;
    }
    return sum + Math.abs(area) / 2;
  }, 0);

  const pixelsPerFoot = 2.5;
  const estimatedSqft = Math.round(totalPixels / (pixelsPerFoot * pixelsPerFoot));

  return (
    <div className="space-y-4">
      <div className="flex items-center justify-between">
        <div className="flex items-center gap-2">
          <div className="flex items-center bg-zinc-900 border border-zinc-700 rounded-lg overflow-hidden">
            <button onClick={() => setTool("draw")} className={`flex items-center gap-1.5 px-3 py-2 text-xs font-medium transition-colors ${tool === "draw" ? "bg-red-500/20 text-red-400" : "text-zinc-500 hover:text-white"}`}>
              <Pencil className="w-3.5 h-3.5" /> Draw
            </button>
            <button onClick={() => setTool("pointer")} className={`flex items-center gap-1.5 px-3 py-2 text-xs font-medium transition-colors ${tool === "pointer" ? "bg-red-500/20 text-red-400" : "text-zinc-500 hover:text-white"}`}>
              <MousePointer2 className="w-3.5 h-3.5" /> Select
            </button>
          </div>
          <button onClick={() => { if (points.length > 0) setPoints(prev => prev.slice(0, -1)); else if (allShapes.length > 0) setAllShapes(prev => prev.slice(0, -1)); }} className="flex items-center gap-1 px-3 py-2 text-xs text-zinc-500 hover:text-white bg-zinc-900 border border-zinc-700 rounded-lg transition-colors">
            <Undo2 className="w-3.5 h-3.5" /> Undo
          </button>
          <button onClick={() => { setPoints([]); setAllShapes([]); }} className="flex items-center gap-1 px-3 py-2 text-xs text-zinc-500 hover:text-white bg-zinc-900 border border-zinc-700 rounded-lg transition-colors">
            <Trash2 className="w-3.5 h-3.5" /> Clear
          </button>
        </div>
        {allShapes.length > 0 && (
          <span className="text-xs font-mono text-red-400 bg-red-500/10 border border-red-500/20 px-3 py-1.5 rounded-lg">
            ~{estimatedSqft.toLocaleString()} sq ft traced
          </span>
        )}
      </div>

      <div ref={containerRef} className="relative rounded-xl overflow-hidden border-2 border-zinc-700" style={{ aspectRatio: "16/10" }}>
        <iframe src={satelliteUrl} className="absolute inset-0 w-full h-full" style={{ border: 0 }} loading="lazy" referrerPolicy="no-referrer-when-downgrade" title="Satellite view" />
        <canvas
          ref={canvasRef}
          width={800}
          height={500}
          onClick={handleCanvasClick}
          className="absolute inset-0 w-full h-full"
          style={{ cursor: tool === "draw" ? "crosshair" : "default" }}
        />
        {points.length === 0 && allShapes.length === 0 && (
          <div className="absolute inset-0 flex items-center justify-center pointer-events-none">
            <div className="bg-black/70 backdrop-blur-sm rounded-xl px-6 py-4 text-center">
              <Pencil className="w-6 h-6 text-red-500 mx-auto mb-2" />
              <p className="text-sm font-semibold text-white">Click to trace your roof outline</p>
              <p className="text-[10px] text-zinc-400 mt-1">Click points around your roof edges. Click near the first point to close the shape.</p>
            </div>
          </div>
        )}
      </div>

      <div className="flex items-center justify-between">
        <p className="text-[10px] text-zinc-600">
          {allShapes.length === 0 && points.length === 0 && "Trace your roof edges on the satellite image. Close shapes by clicking near the starting point."}
          {points.length > 0 && `${points.length} points placed. Click near the first point to close this shape.`}
          {allShapes.length > 0 && points.length === 0 && `${allShapes.length} shape${allShapes.length > 1 ? "s" : ""} traced. Draw more or continue to estimate.`}
        </p>
        <button
          onClick={() => {
            const canvas = canvasRef.current;
            const dataUrl = canvas ? canvas.toDataURL("image/png") : "";
            onComplete(dataUrl, estimatedSqft > 0 ? estimatedSqft : 2000);
          }}
          className="flex items-center gap-2 px-5 py-2.5 bg-red-500 hover:bg-red-600 text-white text-sm font-semibold rounded-lg transition-colors"
        >
          {allShapes.length > 0 ? "Use Traced Area" : "Skip — Enter Manually"} <ArrowRight className="w-4 h-4" />
        </button>
      </div>
    </div>
  );
}

